    public ProposalAbstractTab() {
        initComponents();
        
        //Added by Ajay since there was no focus trversal once Cntrl+T was pressed  24-8-2004
        java.awt.Component[] components = { this, txtArAbstract };
        ScreenFocusTraversalPolicy traversePolicy = new ScreenFocusTraversalPolicy( components );
        this.setFocusTraversalPolicy(traversePolicy);
        this.setFocusCycleRoot(true);
        //End
    }
     
    
    /** Creates new form ProposalAbstractTab using Abstract Text
     * @param absText Abstract text string
     */
    public ProposalAbstractTab(String absText) {
        this();
        txtArAbstract.setCaretPosition(0);
        setAbstractText(absText);
    }
    
    /** This method is used to get the String entered in the text area
     * @return Abstract Text
     */
    public String getAbstractText(){
        return ((txtArAbstract.getText().length() == 0) ? null : txtArAbstract.getText());
    }
    
    public javax.swing.JTextArea getTextArea(){
        return txtArAbstract;
    }
    /** This method is used to set a String to the text area
     * @param absText Sets the Text area to the bean
     */
    public void setAbstractText(String absText){
        txtArAbstract.setText(absText);
    }
