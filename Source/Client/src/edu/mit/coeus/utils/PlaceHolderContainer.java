   /**@param vector of nodes
    *sets the custonmized data with the customized tags
    */     
    public void setData(Vector nodes) {
        this.vecNodes = nodes;
        String node;
        //attributeNode;
        NamedNodeMap map;
        int nodeCount = nodes.size();
        
        arrPlaceHolder = new PlaceHolder[nodeCount];
        PlaceHolder panel;
        String label; 
        for(int index = 0; index < nodeCount; index++) {
            node = (String)nodes.get(index);
            panel = new PlaceHolder();
            panel.setData(node);
            arrPlaceHolder[index] = panel;
            tbdPlaceHolder.addTab(panel.getPlaceHolderName(),panel);
        }
    }
    
