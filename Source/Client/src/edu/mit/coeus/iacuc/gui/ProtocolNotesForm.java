    public void  preparePanel(Vector vecProtocolNotes) {
        
        //pnlMainPanel.setLayout(new BoxLayout(pnlMainPanel, BoxLayout.Y_AXIS));
        pnlMainPanel.setLayout(new GridLayout(vecProtocolNotes.size(),1,0,10));
        ProtocolNotes protocolNotes;
        int size = vecProtocolNotes.size();
        for(int index=0;index<vecProtocolNotes.size();++index){
            ProtocolNotepadBean notepadBean = ( ProtocolNotepadBean ) vecProtocolNotes.elementAt(index);
            protocolNotes = new ProtocolNotes(notepadBean);
            pnlMainPanel.add(protocolNotes.getChildPanel());
        }
    }

