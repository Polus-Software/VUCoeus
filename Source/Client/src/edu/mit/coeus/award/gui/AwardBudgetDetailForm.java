    private void scrPnAwardBudgetDetailFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_scrPnAwardBudgetDetailFocusGained
          if(tblAwardDetail.getRowCount() > 0){
              String costElement = (String)tblAwardDetail.getValueAt(0, 1);
              if(costElement.trim().equals("")){
                  tblAwardDetail.setRowSelectionInterval(0,0);
                  tblAwardDetail.setColumnSelectionInterval(1,1);
                  tblAwardDetail.editCellAt(0, 1);
                  tblAwardDetail.getEditorComponent().requestFocusInWindow();
              }else{
                  tblAwardDetail.setRowSelectionInterval(0,0);
                  tblAwardDetail.setColumnSelectionInterval(3,3);
                  tblAwardDetail.editCellAt(0, 3);
                  tblAwardDetail.getEditorComponent().requestFocusInWindow();
              }
           }
    }//GEN-LAST:event_scrPnAwardBudgetDetailFocusGained
    
    
