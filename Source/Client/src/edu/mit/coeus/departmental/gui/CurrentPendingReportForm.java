    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        if((radiobtnCurrent.isSelected() == true) || (radiobtnPending.isSelected() == true) || (radiobtnBoth.isSelected() == true)) {
            if(radiobtnCurrent.isSelected() == true) {
                CurrentAndPendingReportDetailForm.saveAsActiveSheetCurrent();
            } else if(radiobtnPending.isSelected() == true){
                CurrentAndPendingReportDetailForm.saveAsActiveSheetPending();
            } else if(radiobtnBoth.isSelected() == true){
                SaveAsDialog.isXLSFile = true;
                CurrentAndPendingReportDetailForm.saveAsActiveSheetBoth();
            }
            //COEUSQA-1686 : Add additional fields to the Current Pending Support Schema - To set Current radio button as default - Start                     
            radiobtnCurrent.setSelected(true);
            radiobtnPending.setSelected(false);
            radiobtnBoth.setSelected(false);
            //COEUSQA-1686 : End 
            CurrentPendingReportDialog.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(null, "Select any one option");
        }
    }//GEN-LAST:event_btnOkActionPerformed
    
    
