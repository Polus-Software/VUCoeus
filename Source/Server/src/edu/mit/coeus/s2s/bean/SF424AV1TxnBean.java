    public CoeusVector getBalanceOfFunds(String propNumber)
        throws CoeusException, DBException {
     
        BigDecimal cost = null;
        String period = null;
        HashMap hmAmts = null;
        
        if(propNumber==null) 
                throw new CoeusXMLException("Proposal Number is Null");   
        
       CoeusVector cvAmts = new CoeusVector();
       Vector result = null;
        Vector param= new Vector();

        HashMap row = null;
        param.addElement(new Parameter("PROPOSAL_NUMBER",
                       DBEngineConstants.TYPE_STRING,propNumber));
        if(dbEngine !=null){
           result = new Vector(3,2);
           result = dbEngine.executeRequest("Coeus",
               "call s2sSF424AV1Pkg.getBalanceOfFunds( <<PROPOSAL_NUMBER>> , "
                                + " <<OUT RESULTSET rset>> )", "Coeus", param);
        }else{
                throw new CoeusException("db_exceptionCode.1000");
        }
        
           int vecSize = result.size();
           hmAmts = new HashMap();
           
           if (vecSize >0){
            for(int i=0;i<vecSize;i++){
                row = (HashMap) result.elementAt(i);          
                cost = new BigDecimal( row.get("COST").toString());
                period = row.get("PERIOD").toString();
                hmAmts = new HashMap();
                hmAmts.put("PERIOD",period);
                hmAmts.put("COST",cost);
                cvAmts.add(hmAmts);

               }
           }
        
       return cvAmts;
    }
    
