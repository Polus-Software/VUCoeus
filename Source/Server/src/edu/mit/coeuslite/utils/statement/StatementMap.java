package edu.mit.coeuslite.utils.statement;

/**
 *
 * @author  sharathk
 */
public class StatementMap {
    
    private String parameter;
    private String parameterType;
    private String field;
    
    /** Creates a new instance of StatementMap */
    public StatementMap() {
    }
    
    /**
     * Getter for property parameter.
     * @return Value of property parameter.
     */
    public java.lang.String getParameter() {
        return parameter;
    }
    
    /**
     * Setter for property parameter.
     * @param parameter New value of property parameter.
     */
    public void setParameter(java.lang.String parameter) {
        this.parameter = parameter;
    }
    
    /**
     * Getter for property parameterType.
     * @return Value of property parameterType.
     */
    public java.lang.String getParameterType() {
        return parameterType;
    }
    
    /**
     * Setter for property parameterType.
     * @param parameterType New value of property parameterType.
     */
    public void setParameterType(java.lang.String parameterType) {
        this.parameterType = parameterType;
    }
    
    /**
     * Getter for property field.
     * @return Value of property field.
     */
    public java.lang.String getField() {
        return field;
    }
    
    /**
     * Setter for property field.
     * @param field New value of property field.
     */
    public void setField(java.lang.String field) {
        this.field = field;
    }
    
}
